'''输出数字的加减乘除运算结果'''
a, b, c = 2, 3, '结果'  # 定义变量时可以一起定义，按先后顺序赋值
print(f'a+b{c}:{a + b}')
### a+b结果:5
print(f'a*b{c}:{a * b}')
### a*b结果:6
print(f'a/b{c}:{a / b}')
### a/b结果:0.6666666666666666
print(f'a的平方:{a ** 2}')  # **2代表平方，**3代表立方
### a的平方:4
print(f'b的立方:%d' % b ** 3)
### b的立方:27
print(2 + 3)
### 5


